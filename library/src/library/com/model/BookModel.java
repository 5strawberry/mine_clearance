package library.com.model;

public class BookModel {
	private String book_name;
	private String book_number;
	private String book_prince;
	private String book_author;
	private String publishment;
	private String borrow;
	
	public String getBook_name() {
		return book_name;
	}
	public void setBook_name(String book_name) {
		this.book_name = book_name;
	}
	public String getBook_number() {
		return book_number;
	}
	public void setBook_number(String book_number) {
		this.book_number = book_number;
	}
	public String getBook_prince() {
		return book_prince;
	}
	public void setBook_prince(String i) {
		this.book_prince =  i;
	}
	public String getBook_author() {
		return book_author;
	}
	public void setBook_author(String book_author) {
		this.book_author = book_author;
	}
	public String getPublishment() {
		return publishment;
	}
	public void setPublishment(String publishment) {
		this.publishment = publishment;
	}
	public String getBorrow() {
		return borrow;
	}
	public void setBorrow(String borrow) {
		this.borrow = borrow;
	}

	
}
